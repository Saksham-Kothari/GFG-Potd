class Solution {
    // Function to rotate matrix anticlockwise by 90 degrees.
     static void rotateby90(int mat[][]) {

       int n = mat.length;

    //  Find the transpose of the matrix
    for (int i = 0; i < n; i++) {
        for (int j = i; j < n; j++) {
            int temp = mat[i][j];
            mat[i][j] = mat[j][i];
            mat[j][i] = temp;
        }
    }

    //  Swap the top and bottom row elements for each column
    for (int i = 0; i < n; i++) {
        int toi = 0;
        int doi = n - 1;
        while (toi < doi) {
            int temp = mat[toi][i];
            mat[toi][i] = mat[doi][i];
            mat[doi][i] = temp;

            toi++;
            doi--;
        }
    }

    }
}